/** Convierte Bs → moneda elegida */
export function bobA(montoBob, code = 'USD') {
  const m = monedas.find((x) => x.code === code) || monedas[1];
  const val = montoBob / m.porBob;
  return { ...m, valor: val, texto: `${m.symbol} ${val.toFixed(m.porBob < 0.01 ? 0 : 2)}` };
}

